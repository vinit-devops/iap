/**
 * Plan assembly and canonical serialization (IEP-0011, ch. 14 §14.5).
 *
 * `buildPlan` is a pure function of exactly its arguments: the desired
 * provider plan, the state snapshot, and the determinism input vector
 * (CP-1). The emitted `content` serializes canonically — compact UTF-8
 * JSON, keys sorted lexicographically by Unicode code point (the same
 * `@iap/model` helper used for document canonical form), waves ordered by
 * dependency rank, entries within a wave sorted by resource identifier
 * (CP-2) — and `planId` is the SHA-256 of exactly those bytes, spelled
 * sha256:<hex>. Timestamps and signatures live only in the (optional)
 * envelope, which is never hashed (§14.5, PL-5); envelopes are created and
 * verified by `./envelope.js` with injected timestamps — never a clock.
 */

import { canonicalJsonStringify, compareCodePoints, sha256Hex } from '@iap/model';
import type { PlanResource, ProviderPlan } from '@iap/provider-sdk';
import {
  PLAN_API_VERSION,
  PLANNER_VERSION,
  computeInputsHash,
  computeStateIntegrity,
  sha256Digest,
} from './inputs.js';
import type { DeterminismInputs, PlanInputs, StateSnapshot } from './inputs.js';
import { determineActions } from './lifecycle.js';
import type { PlanAction, PlanActionEntry, ReversibilityClass } from './lifecycle.js';
import { riskRuleTableV1 } from './risk.js';
import { scheduleWaves } from './scheduler.js';

/** Reference to a destructive action (PL-3: always listed, never implied). */
export interface DestructiveActionRef {
  resource: string;
  action: 'replace' | 'delete';
  reversibility: ReversibilityClass;
}

/** Marker for an attribute whose value cannot appear in plan content. */
export interface UnknownValue {
  resource: string;
  attribute: string;
  /**
   * `output-binding`: generated by the substrate at apply time (bound to an
   * abstract output attribute). `sensitive`: declared sensitive by the
   * provider plan — the redaction rule is that plan content carries
   * attribute names only, never values, and sensitive attributes are
   * additionally marked here so reviewers see that a secret-bearing
   * attribute participates without ever seeing material (CE-6, PL-5).
   */
  reason: 'output-binding' | 'sensitive';
}

export interface RiskFactor {
  id: string;
  weight: number;
  resources: string[];
}

/** Deterministic risk annotation (IEP-0011 risk scoring). */
export interface RiskAnnotation {
  score: number;
  class: 'low' | 'medium' | 'high' | 'critical';
  factors: RiskFactor[];
}

/** Everything risk may depend on: the plan content minus the annotation itself. */
export type RiskInput = Omit<PlanContent, 'risk'>;

/**
 * The risk-scoring seam. Risk is a pure function of the plan content
 * (IEP-0011): the annotator receives every other content member and must
 * derive the annotation from nothing else — no clock, no environment, no
 * inference. The default is `riskRuleTableV1` (`./risk.js`), whose version
 * folds into `plannerVersion`; an injected replacement changes hashed
 * content and therefore every planId.
 */
export type RiskAnnotator = (content: RiskInput) => RiskAnnotation;

/** Cost delta with explicit unavailable/deferred markers (design decision 5). */
export interface CostDelta {
  status: 'unavailable' | 'deferred';
  reason: 'no-pricing-snapshot' | 'pricing-deferred-phase-10';
  pricingSnapshot?: string;
}

/** One security-relevant field change derived from the plan itself. */
export interface SecurityDelta {
  resource: string;
  field: string;
  /** Canonical document source path under spec.exposure or spec.encryption. */
  source: string;
}

export interface ComplianceFinding {
  framework: string;
  resource: string;
  detail: string;
}

/** Compliance delta: empty with an explicit deferred marker until Phase 11. */
export interface ComplianceDelta {
  deferred: 'phase-11';
  findings: ComplianceFinding[];
}

export interface PlanDeltas {
  cost: CostDelta;
  security: SecurityDelta[];
  compliance: ComplianceDelta;
}

export interface RollbackLimitation {
  resource: string;
  reason: string;
}

/**
 * Rollback contract (ch. 14 §14.6): the only strategy is re-planning to a
 * prior succeeded revision. `limitations` names every action that
 * re-planning cannot undo — one entry per irreversible action (delete of a
 * stateful resource with no restore source visible to the plan), so
 * unsupported rollback is always explicitly reported (Phase 7 exit
 * criterion 4), never discovered at rollback time.
 */
export interface PlanRollback {
  strategy: 're-plan-to-revision';
  limitations: RollbackLimitation[];
}

export const VERIFICATION_CHECKS = {
  create: 'created',
  'update-in-place': 'attributes-applied',
  replace: 'successor-ready',
  delete: 'removed',
  import: 'adopted',
} as const satisfies Record<PlanAction, string>;

export interface VerificationEntry {
  resource: string;
  check: (typeof VERIFICATION_CHECKS)[PlanAction];
}

/** Approval gate protecting one resource before execution (ch. 19 §19.6). */
export interface ApprovalRequirement {
  resource: string;
  gate: ApprovalGate;
}

/** Closed approval-gate vocabulary: one gate per destructive action. */
export const APPROVAL_GATES = [
  'stateful-replace',
  'stateful-delete',
  'destructive-replace',
  'destructive-delete',
] as const;
export type ApprovalGate = (typeof APPROVAL_GATES)[number];

/**
 * The deterministic approval rule: every destructive action requires
 * exactly one gate (IEP-0011: every destructiveActions entry carries its
 * required approval gate). Statefulness is read off the reversibility
 * class — the data-bearing classes map to the human gates of ch. 19 §19.6
 * (stateful replacement) and ch. 14 §14.6 (explicit data-loss acceptance);
 * the replacement-recoverable classes map to the plain destructive gates.
 * Total over the closed vocabularies; never derived from the risk class
 * (risk is computed over content that already includes these entries).
 */
export function approvalGateOf(
  action: 'replace' | 'delete',
  reversibility: ReversibilityClass,
): ApprovalGate {
  const dataBearing =
    reversibility === 'reversible-with-data-risk' ||
    reversibility === 'manual-recovery-required' ||
    reversibility === 'irreversible';
  if (action === 'replace') return dataBearing ? 'stateful-replace' : 'destructive-replace';
  return dataBearing ? 'stateful-delete' : 'destructive-delete';
}

/** The hashed plan content — canonical bytes of exactly this yield planId. */
export interface PlanContent {
  inputs: PlanInputs;
  waves: PlanActionEntry[][];
  destructiveActions: DestructiveActionRef[];
  unknownValues: UnknownValue[];
  risk: RiskAnnotation;
  deltas: PlanDeltas;
  rollback: PlanRollback;
  verification: VerificationEntry[];
  approvalsRequired: ApprovalRequirement[];
}

/** Envelope: timestamps and signature around the hashed content (never hashed). */
export interface PlanEnvelope {
  createdAt: string;
  expiresAt: string;
  signature: { keyId: string; alg: 'ed25519'; value: string };
}

/** The complete plan.iap.dev/v1 artifact. */
export interface PlanArtifact {
  apiVersion: typeof PLAN_API_VERSION;
  planId: string;
  envelope?: PlanEnvelope;
  content: PlanContent;
}

