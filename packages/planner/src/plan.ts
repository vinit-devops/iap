/**
 * Caller-supplied identities the `plan()` facade cannot derive from its
 * artifact arguments. Everything derivable is derived — documentHash,
 * target, and mappingVersions from the provider plan; stateRevision and
 * stateIntegrity from the snapshot; plannerVersion from this package — so
 * the facade cannot be handed a vector that contradicts its artifacts.
 */
export interface InputIdentities {
  /** Identity 2: profile name → sha256:<hex> of each merged profile definition. */
  profileHashes?: Record<string, string>;
  /** Identity 3: policy bundle name → version/hash in force. */
  policyBundles?: Record<string, string>;
  /** Identity 4: extension package namespace → version. */
  extensionVersions?: Record<string, string>;
  /** Identity 6: discovery snapshot id (never a live lookup — ch. 12 §12.2). */
  discoverySnapshot?: string | null;
  /** Identity 7: pricing snapshot id; omitted ⇒ deltas.cost is unavailable. */
  pricingSnapshot?: string | null;
}

export interface PlanOptions {
  /** Risk-scoring seam; defaults to the versioned rule table (riskRuleTableV1). */
  risk?: RiskAnnotator;
}

/**
 * The current nine-element identity vector for a (provider plan, snapshot,
 * caller identities) triple — everything derivable is derived, so the
 * vector can never contradict its artifacts. Shared by the `plan()` facade
 * and by `verifyPlan`, which recomputes exactly this vector before
 * execution (PL-2).
 */
export function deriveDeterminismInputs(
  providerPlan: ProviderPlan,
  stateSnapshot: StateSnapshot,
  identities: InputIdentities = {},
): DeterminismInputs {
  return {
    documentHash: `sha256:${providerPlan.documentHash}`,
    target: { provider: providerPlan.provider, profile: providerPlan.profile },
    profileHashes: identities.profileHashes ?? {},
    policyBundles: identities.policyBundles ?? {},
    extensionVersions: identities.extensionVersions ?? {},
    mappingVersions: { [providerPlan.provider]: providerPlan.mappingVersion },
    discoverySnapshot: identities.discoverySnapshot ?? null,
    pricingSnapshot: identities.pricingSnapshot ?? null,
    stateRevision: stateSnapshot.revision,
    stateIntegrity: stateSnapshot.integrity,
    plannerVersion: PLANNER_VERSION,
  };
}

/**
 * The planner facade: desired provider plan + state snapshot + explicit
 * input identities → a complete, schema-valid plan.iap.dev/v1 artifact
 * (without envelope — `signPlan` attaches one with injected timestamps).
 */
export function plan(
  providerPlan: ProviderPlan,
  stateSnapshot: StateSnapshot,
  identities: InputIdentities = {},
  options: PlanOptions = {},
): PlanArtifact {
  const content = buildPlan({
    desired: providerPlan,
    state: stateSnapshot,
    inputs: deriveDeterminismInputs(providerPlan, stateSnapshot, identities),
    ...(options.risk !== undefined ? { risk: options.risk } : {}),
  });
  return { apiVersion: PLAN_API_VERSION, planId: computePlanId(content), content };
}
