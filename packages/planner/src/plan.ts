/** Canonical byte projection of plan content (CP-2). */
export function canonicalPlanSerialization(content: PlanContent): string {
  return canonicalJsonStringify(content);
}

/** planId = sha256:<hex> over the canonical content serialization (§14.5). */
export function computePlanId(content: PlanContent): string {
  return sha256Digest(canonicalPlanSerialization(content));
}

function refuse(condition: boolean, message: string): void {
  if (condition) throw new TypeError(`planner input refused: ${message}`);
}

/**
 * Fail-closed input verification: the supplied artifacts must be internally
 * consistent and must be the artifacts the identity vector describes (the
 * plan-side counterpart of CP-4 — a planner never plans over inputs it
 * cannot vouch for).
 */
function verifyInputs(
  desired: ProviderPlan,
  state: StateSnapshot,
  inputs: DeterminismInputs,
): void {
  refuse(
    desired.formatVersion !== 1,
    `unsupported provider plan formatVersion ${String(desired.formatVersion)}`,
  );
  const unhashed: Record<string, unknown> = { ...desired };
  delete unhashed.planHash;
  refuse(
    sha256Hex(canonicalJsonStringify(unhashed)) !== desired.planHash,
    'provider plan planHash does not verify against its content',
  );
  refuse(
    computeStateIntegrity(state.objects) !== state.integrity,
    'state snapshot integrity hash does not verify against its objects (ch. 13 §13.1: treated as corruption)',
  );
  refuse(
    inputs.documentHash !== `sha256:${desired.documentHash}`,
    'inputs.documentHash does not match the provider plan documentHash',
  );
  refuse(
    inputs.target.provider !== desired.provider,
    'inputs.target.provider does not match the provider plan',
  );
  refuse(
    inputs.target.profile !== desired.profile,
    'inputs.target.profile does not match the provider plan profile',
  );
  refuse(
    inputs.mappingVersions[desired.provider] !== desired.mappingVersion,
    `inputs.mappingVersions must record ${desired.provider}@${desired.mappingVersion}`,
  );
  refuse(
    inputs.stateRevision !== state.revision,
    'inputs.stateRevision does not match the state snapshot revision',
  );
  refuse(
    inputs.stateIntegrity !== state.integrity,
    'inputs.stateIntegrity does not match the state snapshot integrity',
  );
  refuse(
    inputs.plannerVersion !== PLANNER_VERSION,
    `inputs.plannerVersion must be this implementation's version (${PLANNER_VERSION})`,
  );
}

function collectUnknownValues(desired: ProviderPlan, actions: PlanActionEntry[]): UnknownValue[] {
  const scheduled = new Set(
    actions.filter((entry) => entry.action !== 'delete').map((entry) => entry.resource),
  );
  const byKey = new Map<string, UnknownValue>();
  const add = (value: UnknownValue): void => {
    byKey.set(`${value.resource} ${value.attribute} ${value.reason}`, value);
  };
  // Output-bound attributes resolve only at apply time (late-bound).
  for (const bindings of Object.values(desired.outputBindings)) {
    for (const binding of Object.values(bindings)) {
      if (scheduled.has(binding.logicalId)) {
        add({
          resource: binding.logicalId,
          attribute: binding.attribute,
          reason: 'output-binding',
        });
      }
    }
  }
  // Sensitive attributes never enter plan content as values (CE-6, PL-5).
  for (const resource of desired.resources) {
    if (!scheduled.has(resource.logicalId)) continue;
    for (const attribute of resource.sensitiveFields) {
      add({ resource: resource.logicalId, attribute, reason: 'sensitive' });
    }
  }
  return [...byKey.values()].sort(
    (a, b) =>
      compareCodePoints(a.resource, b.resource) ||
      compareCodePoints(a.attribute, b.attribute) ||
      compareCodePoints(a.reason, b.reason),
  );
}

function isSecurityRelevant(source: string): boolean {
  return (
    source === 'spec.exposure' ||
    source.startsWith('spec.exposure.') ||
    source === 'spec.encryption' ||
    source.startsWith('spec.encryption.')
  );
}

function collectSecurityDeltas(
  byId: Map<string, PlanResource>,
  actions: PlanActionEntry[],
): SecurityDelta[] {
  const deltas: SecurityDelta[] = [];
  for (const entry of actions) {
    const resource = byId.get(entry.resource);
    if (resource === undefined) continue; // deletes carry no desired-side provenance
    for (const field of entry.fields) {
      const source = resource.provenance[field]?.source;
      if (source !== undefined && isSecurityRelevant(source)) {
        deltas.push({ resource: entry.resource, field, source });
      }
    }
  }
  return deltas.sort(
    (a, b) => compareCodePoints(a.resource, b.resource) || compareCodePoints(a.field, b.field),
  );
}

function costDelta(inputs: DeterminismInputs): CostDelta {
  if (inputs.pricingSnapshot === null) {
    // Never a silent zero (IEP-0011 cost impact; design decision 5).
    return { status: 'unavailable', reason: 'no-pricing-snapshot' };
  }
  // A snapshot is pinned, but pricing computation lands with Phase 10.
  return {
    status: 'deferred',
    reason: 'pricing-deferred-phase-10',
    pricingSnapshot: inputs.pricingSnapshot,
  };
}

export interface BuildPlanOptions {
  /** Desired provider-resource model from `applyMapping` (IEP-0012). */
  desired: ProviderPlan;
  /** Actual state (IEP-0010 subset); `emptySnapshot()` for first deployment. */
  state: StateSnapshot;
  /** The nine-element determinism input vector; `inputsHash` is computed here. */
  inputs: DeterminismInputs;
  /** Risk-scoring seam; defaults to the versioned rule table (riskRuleTableV1). */
  risk?: RiskAnnotator;
}

/**
 * Assemble the full `content` object: diff → lifecycle actions → waves →
 * destructive/unknown/delta/rollback/verification members → risk
 * annotation. Every collection is emitted in its documented deterministic
 * order, so `canonicalPlanSerialization(content)` is byte-stable across
 * runs, machines, environments, and key orderings (CP-2/CP-3).
 */
export function buildPlan(options: BuildPlanOptions): PlanContent {
  const { desired, state, inputs } = options;
  verifyInputs(desired, state, inputs);

  const actions = determineActions(desired, state);
  const waves = scheduleWaves(actions, desired, state);
  const byId = new Map<string, PlanResource>(
    desired.resources.map((resource) => [resource.logicalId, resource]),
  );

  const destructiveActions: DestructiveActionRef[] = actions
    .filter((entry) => entry.destructive)
    .map((entry) => ({
      resource: entry.resource,
      action: entry.action as 'replace' | 'delete',
      reversibility: entry.reversibility,
    }))
    .sort((a, b) => compareCodePoints(a.resource, b.resource));

  const verification: VerificationEntry[] = actions
    .map((entry) => ({ resource: entry.resource, check: VERIFICATION_CHECKS[entry.action] }))
    .sort((a, b) => compareCodePoints(a.resource, b.resource));

  // Unsupported rollback is explicitly reported (§14.6; exit criterion 4):
  // one limitation per irreversible action — re-planning to a prior revision
  // recreates the resource but cannot restore data no plan input can see.
  const limitations: RollbackLimitation[] = actions
    .filter((entry) => entry.reversibility === 'irreversible')
    .map((entry) => ({
      resource: entry.resource,
      reason: `irreversible ${entry.action} of a stateful resource: no restore source is visible to the plan, so re-planning to a prior revision cannot recover its data (ch. 14 §14.6)`,
    }))
    .sort((a, b) => compareCodePoints(a.resource, b.resource));

  // Every destructive action requires exactly one approval gate (IEP-0011;
  // ch. 19 §19.6). Non-destructive actions require none.
  const approvalsRequired: ApprovalRequirement[] = destructiveActions
    .map((entry) => ({
      resource: entry.resource,
      gate: approvalGateOf(entry.action, entry.reversibility),
    }))
    .sort((a, b) => compareCodePoints(a.resource, b.resource) || compareCodePoints(a.gate, b.gate));

  const planInputs: PlanInputs = {
    documentHash: inputs.documentHash,
    target: { provider: inputs.target.provider, profile: inputs.target.profile },
    profileHashes: sortRecord(inputs.profileHashes),
    policyBundles: sortRecord(inputs.policyBundles),
    extensionVersions: sortRecord(inputs.extensionVersions),
    mappingVersions: sortRecord(inputs.mappingVersions),
    discoverySnapshot: inputs.discoverySnapshot,
    pricingSnapshot: inputs.pricingSnapshot,
    stateRevision: inputs.stateRevision,
    stateIntegrity: inputs.stateIntegrity,
    plannerVersion: inputs.plannerVersion,
    inputsHash: computeInputsHash(inputs),
  };

  const base: RiskInput = {
    inputs: planInputs,
    waves,
    destructiveActions,
    unknownValues: collectUnknownValues(desired, actions),
    deltas: {
      cost: costDelta(inputs),
      security: collectSecurityDeltas(byId, actions),
      compliance: { deferred: 'phase-11', findings: [] },
    },
    rollback: { strategy: 're-plan-to-revision', limitations },
    verification,
    approvalsRequired,
  };

  const annotate = options.risk ?? riskRuleTableV1;
  return { ...base, risk: annotate(base) };
}

function sortRecord(record: Record<string, string>): Record<string, string> {
  const sorted: Record<string, string> = {};
  for (const key of Object.keys(record).sort(compareCodePoints)) {
    sorted[key] = record[key] as string;
  }
  return sorted;
}

